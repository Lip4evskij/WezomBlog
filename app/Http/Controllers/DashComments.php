     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $comm=new Comm();
        $comm->name=$request->name;
        $comm->posts_id=$request->posts_id;
        $comm->text=$request->text;
        $comm->save();
        return back();
    }

